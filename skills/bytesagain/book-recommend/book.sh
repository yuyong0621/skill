|------|--------|--------|
| 💼 商业 | 5本 | 管理、创业、营销 |
| 🔧 科技 | 5本 | AI、编程、产品 |
| 🧠 思维 | 5本 | 心理学、决策、认知 |
| 📖 文学 | 5本 | 小说、散文、传记 |
| 🌍 社科 | 5本 | 历史、经济、社会 |

用 \`book.sh annual ${year} business\` 查看具体分类

### 📊 ${year}阅读趋势

1. **AI相关书籍爆发** — 大模型、AIGC、AI应用
2. **心理自助类持续热门** — 焦虑、内卷、自我疗愈
3. **东方哲学回归** — 中国传统文化、禅宗
4. **短阅读兴起** — 碎片化阅读、知识卡片
5. **音频书增长** — 通勤听书成为习惯
EOF
      ;;
  esac
}

case "$CMD" in
  recommend)    cmd_recommend "$@" ;;
  summary)      cmd_summary "$@" ;;
  similar)      cmd_similar "$@" ;;
  reading-list) cmd_reading_list "$@" ;;
  club)         cmd_club "$@" ;;
  annual)       cmd_annual "$@" ;;
  help|--help)  show_help ;;
  *)
    echo "❌ 未知命令: $CMD"
    echo "运行 'book.sh help' 查看帮助"
    exit 1
    ;;
esac
