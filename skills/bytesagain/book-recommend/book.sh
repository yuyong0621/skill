#!/usr/bin/env bash
set -euo pipefail

# Book Recommend — 书单推荐助手
# Usage: bash scripts/book.sh <command> [args...]

CMD="${1:-help}"
shift 2>/dev/null || true

show_help() {
  cat <<'EOF'
Book Recommend — 书单推荐助手

Commands:
  recommend <interests> [type] [level]     按兴趣推荐
  summary <book_name>                     书籍摘要
  similar <book_name>                     类似推荐
  reading-list <topic> [count]            主题阅读清单
  club <book_name> [duration]             读书会方案
  annual <year> [category]                年度书单
  help                                   显示帮助

Examples:
  book.sh recommend "创业,商业模式" "non-fiction" "intermediate"
  book.sh summary "思考，快与慢"
  book.sh similar "人类简史"
  book.sh reading-list "产品经理入门" "10"
  book.sh club "原则" "4weeks"
  book.sh annual "2024" "business"

  Powered by BytesAgain | bytesagain.com | hello@bytesagain.com
EOF
}

cmd_recommend() {
  local interests="${1:?请提供兴趣关键词 (逗号分隔)}"
  local book_type="${2:-any}"
  local level="${3:-any}"

  cat <<EOF
## 📚 个性化书籍推荐

**兴趣**: ${interests}
**类型**: ${book_type}
**难度**: ${level}

---

### 🎯 精选推荐

| # | 书名 | 作者 | 推荐理由 | 难度 |
|---|------|------|---------|------|
| 1 | (根据兴趣匹配) | - | 与"${interests}"高度相关 | ⭐⭐ |
| 2 | (根据兴趣匹配) | - | 经典入门读物 | ⭐⭐ |
| 3 | (根据兴趣匹配) | - | 新锐观点 | ⭐⭐⭐ |
| 4 | (根据兴趣匹配) | - | 实践指南 | ⭐⭐ |
| 5 | (根据兴趣匹配) | - | 深度思考 | ⭐⭐⭐⭐ |

### 📖 阅读建议

EOF

  case "$level" in
    beginner|入门)
      cat <<'EOF'
**入门阶段建议:**
- 从畅销通俗读物开始，建立兴趣
- 每本书先读目录和结论，建立框架
- 不必追求读完，找到对你有用的部分
- 每周一本轻读物的节奏
EOF
      ;;
    intermediate|进阶)
      cat <<'EOF'
**进阶阶段建议:**
- 开始读经典和原著
- 做读书笔记，画思维导图
- 同一主题读3-5本，形成体系
- 尝试写读后感或分享
EOF
      ;;
    advanced|高级)
      cat <<'EOF'
**高级阶段建议:**
- 挑战学术著作和原版书
- 批判性阅读，形成自己的观点
- 跨学科阅读，寻找交叉点
- 输出倒逼输入（写文章/做分享）
EOF
      ;;
    *)
      echo "**通用建议**: 先读薄的，再读厚的；先读通俗的，再读专业的。"
      ;;
  esac

  echo ""
  echo "💡 用 \`book.sh similar <书名>\` 获取类似推荐"
}

