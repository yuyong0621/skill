cmd_club() {
  local book="${1:?请提供书名}"
  local duration="${2:-4weeks}"

  local weeks="${duration//[!0-9]/}"
  (( weeks == 0 )) && weeks=4

  cat <<EOF
## 📖 读书会方案: ${book}

### 基本信息

| 项目 | 内容 |
|------|------|
| 共读书目 | ${book} |
| 活动周期 | ${weeks}周 |
| 频率 | 每周1次讨论 |
| 每次时长 | 60-90分钟 |
| 建议人数 | 5-12人 |

---

### 📅 周计划

EOF

  for ((w=1; w<=weeks; w++)); do
    local section_pct=$(( 100 / weeks ))
    local from=$(( (w-1) * section_pct + 1 ))
    local to=$(( w * section_pct ))
    (( w == weeks )) && to=100

    cat <<EOF
#### 第${w}周: 阅读 ${from}%-${to}%

**📚 阅读任务**: 阅读全书的第${from}%-${to}%
**🤔 思考问题**:
1. 这部分最打动你的是什么？
2. 你同意作者的观点吗？为什么？
3. 这些观点如何应用到你的生活/工作中？

**💬 讨论环节** (60分钟):
- 10min: 破冰 + 整体感受分享
- 30min: 核心问题讨论
- 15min: 实际应用探讨
- 5min: 下周预告 + 作业

EOF
  done

  cat <<'EOF'
### 🎯 读书会成功秘诀

1. **提前指定领读人** — 每次由一人主持
2. **准备开放式问题** — 没有标准答案的问题最能引发讨论
3. **鼓励不同声音** — 欢迎反对意见
4. **控制时间** — 避免跑题太远
5. **记录要点** — 每次讨论留下总结

### 📝 破冰问题模板

- "用一个词概括你读这段的感受"
- "如果你是主角，你会怎么做？"
- "最让你惊讶/困惑的部分是？"
- "有没有在现实中遇到类似情况？"
EOF
}

