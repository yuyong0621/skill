  echo ""

  case "$category" in
    business|商业)
      cat <<EOF
### 💼 商业/管理

| # | 书名 | 关键词 | 推荐指数 |
|---|------|--------|---------|
| 1 | (年度商业畅销) | 商业思维 | ⭐⭐⭐⭐⭐ |
| 2 | (管理经典新版) | 领导力 | ⭐⭐⭐⭐ |
| 3 | (创业故事) | 创业 | ⭐⭐⭐⭐ |
| 4 | (战略洞察) | 战略 | ⭐⭐⭐⭐ |
| 5 | (营销新思) | 营销 | ⭐⭐⭐ |
EOF
      ;;
    tech|科技)
      cat <<EOF
### 🔧 科技/编程

| # | 书名 | 关键词 | 推荐指数 |
|---|------|--------|---------|
| 1 | (AI/大模型相关) | 人工智能 | ⭐⭐⭐⭐⭐ |
| 2 | (编程方法论) | 软件工程 | ⭐⭐⭐⭐ |
| 3 | (技术趋势) | 前沿科技 | ⭐⭐⭐⭐ |
| 4 | (产品设计) | 产品 | ⭐⭐⭐ |
| 5 | (开源文化) | 开源 | ⭐⭐⭐ |
